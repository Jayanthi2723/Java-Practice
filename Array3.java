class Array3{
  public static void main(String[] args) {
    int[] arr={1,2,3,4,5};
    for(int i=0;i<arr.length;i++){
      System.out.print(arr[i]+" ");
    }
    System.out.println();
    String s[]={"jaya","laya","maya"};
    for(int j=0;j<s.length;j++){
      System.out.println(s[j]);
    }
  }
}
